let soup = "chicken"

switch (soup) {
    case "chicken":
        console.log("I love Chicken soup")
        break;
    
    case "beef":
        console.log("I love Beef soup")
        break
    
    case "fish":
        console.log("I love fish soup")
        break
    default:
        console.log("No soup available")
}

